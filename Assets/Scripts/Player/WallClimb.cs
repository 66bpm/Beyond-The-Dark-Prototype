public class WallClimb : MonoBehaviour
{
    private Movement mm;
    private PlayerController pc;
    private PlayerState ps;
    private Collisions coll;
    private Rigidbody2D rb;

    [Header("Wall Climb Variables")]
    [SerializeField] private float wallClimbModifier = 0.85f;
    private void Awake()
    {
        mm = GetComponent<Movement>();
        pc = GetComponent<PlayerController>();
        ps = GetComponent<PlayerState>();
        coll = GetComponent<Collisions>();
        rb = GetComponent<Rigidbody2D>();
    }
    private void FixedUpdate()
    {
        if (ps.canWallClimb && ps.canControl) DoWallClimb();
    }

    private void DoWallClimb()
    {
        if (coll.hitCeiling)
        {
            rb.gravityScale = 0f;
            rb.velocity = new Vector2(rb.velocity.x, 0f);
        }
        else
        {
            rb.velocity = new Vector2(rb.velocity.x, pc.verticalDirection * mm.maxMovementSpeed * wallClimbModifier);
        }
    }
}
